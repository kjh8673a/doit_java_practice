package src;

public class CreateStringObject {
    public static void main(String[] args) {
        // String 객체 생성 1
        String str1 = new String("안녕"); // new 키워드와 생성자를 이용
        System.out.println(str1);

        // String 객체 생성 2
        String str2 = "안녕하세요"; // 문자열 리터럴을 바로 입력
        System.out.println(str2);
    }
}
